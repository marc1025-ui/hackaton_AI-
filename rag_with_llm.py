    def extract_ui_data_from_llm_response(self, llm_response):
        """
        Extrait les donn√©es JSON du LLM pour l'interface utilisateur
        Le LLM fait directement le filtrage des secteurs pertinents
        """
        try:
            # DEBUG: Afficher la r√©ponse brute du LLM
            print(f"\nüîß DEBUG - R√©ponse brute du LLM:")
            print(f"Type: {type(llm_response)}")
            print(f"Contenu: {str(llm_response)[:500]}...")
            print("-" * 50)

            # Essayer de parser directement comme JSON
            if isinstance(llm_response, str):
                # Nettoyer la r√©ponse si elle contient du texte en plus
                json_start = llm_response.find('{')
                json_end = llm_response.rfind('}') + 1

                if json_start >= 0 and json_end > json_start:
                    json_str = llm_response[json_start:json_end]
                    print(f"üîß DEBUG - JSON extrait: {json_str[:200]}...")

                    data = json.loads(json_str)

                    # V√©rifier que les donn√©es ont la structure attendue
                    if "indicators" in data and isinstance(data["indicators"], list):
                        print(f"‚úÖ JSON valide trouv√© avec {len(data['indicators'])} indicateurs")
                        print(f"ü§ñ Le LLM a filtr√© et retourn√© {len(data['indicators'])} lois pertinentes pour Hutchinson")
                        return data
                    else:
                        print(f"‚ö†Ô∏è  JSON sans structure 'indicators' attendue")

            # Si le parsing √©choue, retourner une structure vide
            print("‚ùå Parsing JSON √©chou√© - structure par d√©faut")
            return {
                "indicators": [],
                "error": "Impossible de parser la r√©ponse LLM"
            }

        except json.JSONDecodeError as e:
            print(f"‚ùå Erreur JSON: {e}")
            return {
                "indicators": [],
                "error": f"R√©ponse LLM non valide (JSON): {str(e)}"
            }
        except Exception as e:
            print(f"‚ùå Erreur g√©n√©rale: {e}")
            return {
                "indicators": [],
                "error": f"Erreur extraction: {str(e)}"
            }

